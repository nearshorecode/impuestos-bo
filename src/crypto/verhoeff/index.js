const tableInv = [0, 4, 3, 2, 1, 5, 6, 7, 8, 9]

const verhoeff = (amount) => {
  const safeAmount = validate(amount)
  const invertedNumber = safeAmount.toString().split('').reverse().join('')
  const chars = invertedNumber.split('')
  let check = 0

  for (let i = 0; i < chars.length; i += 1) {
    const tablePRow = parseInt((i + 1) % 8, 10)
    const tablePColumn = chars[i]
    const k = tableP[tablePRow][tablePColumn]
    const j = check
    check = tableD[j][k]
  }

  return tableInv[check]
}

